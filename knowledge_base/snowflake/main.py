# ── File upload ───────────────────────────────────────────────────────────────

@app.post("/api/upload", tags=["Knowledge Base"])
async def upload_file(file: UploadFile = File(...)):
    """
    Upload an Excel (.xlsx/.xls) or CSV file.
    The file is parsed into a SchemaContext and stored in memory.
    Returns the source_id to use for KB generation and queries.
    """
    allowed = {".csv", ".xlsx", ".xls"}
    suffix  = Path(file.filename or "").suffix.lower()
    if suffix not in allowed:
        raise HTTPException(
            status_code=400,
            detail=f"Unsupported file type '{suffix}'. Allowed: {allowed}",
        )

    content = await file.read()
    try:
        schema, source_id = ingest_file(content, file.filename)
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc))

    # Store schema so the KB generator and pipeline can use it
    _schemas[source_id] = schema

    # If we have a KB for this source, create a pipeline for it
    store = get_store()
    if store.exists(source_id) and source_id not in _pipelines:
        _reload_upload_pipeline(source_id, schema)

    return {
        "source_id": source_id,
        "tables":    [t.name for t in schema.tables],
        "has_kb":    store.exists(source_id),
    }


def _reload_upload_pipeline(source_id: str, schema: SchemaContext) -> None:
    """
    Create (or recreate) an in-memory pipeline for an uploaded file.
    Uses a mock adapter backed by the static schema.
    """
    from adapters.base import DatabaseAdapter, QueryResult
    import time as _time

    class StaticAdapter(DatabaseAdapter):
        """Adapter backed by an in-memory SchemaContext (no real DB)."""
        def __init__(self, ctx: SchemaContext) -> None:
            self._ctx = ctx

        @property
        def dialect(self) -> SQLDialect:
            return self._ctx.dialect

        @property
        def source_name(self) -> str:
            return f"upload:{self._ctx.database}"

        async def connect(self) -> None: pass
        async def disconnect(self) -> None: pass
        async def health_check(self) -> bool: return True

        async def fetch_schema(self, schema_name=None) -> SchemaContext:
            return self._ctx

        async def execute(self, sql: str, timeout=None) -> QueryResult:
            return QueryResult(
                sql=sql, columns=[], rows=[], row_count=0,
                execution_ms=0,
                error="Direct execution not supported for uploaded files. "
                      "Connect to a database to run queries.",
            )

        async def explain(self, sql: str) -> str:
            return ""

    store        = get_store()
    concepts     = store.parse_concepts(source_id)   if store.exists(source_id) else []
    column_notes = store.get_column_notes(source_id) if store.exists(source_id) else {}
    adapter  = StaticAdapter(schema)

    _pipelines[source_id] = NLPToSQLPipeline(
        adapter          = adapter,
        llm              = _llm,
        schema_cache_ttl = 0,
        concept_registry = concepts,
        column_notes     = column_notes,
        source_id        = source_id,
    )
    # Pre-populate schema cache
    from pipeline.nlp_to_sql import _schema_cache
    _schema_cache[adapter.source_name] = (schema, _time.time())
    logger.info("Upload pipeline ready: %s", source_id)


# ── Knowledge Base generation ─────────────────────────────────────────────────

@app.post("/api/generate-kb", tags=["Knowledge Base"])
async def generate_kb(req: GenerateKBRequest):
    """
    Generate (or regenerate) the markdown knowledge base for a source.
    Uses Llama to analyse the schema and write a business knowledge document.
    The model unloads from VRAM when done (sequential loading pattern).
    """
    source_id = req.source_id
    domain    = req.domain

    # Resolve schema — from DB adapter or uploaded file
    schema: Optional[SchemaContext] = None

    if source_id in _adapters:
        adapter = _adapters[source_id]
        try:
            schema = await get_schema_cached(adapter)
        except Exception as exc:
            raise HTTPException(status_code=503, detail=f"Schema fetch failed: {exc}")

    elif source_id in _schemas:
        schema = _schemas[source_id]

    else:
        raise HTTPException(
            status_code=404,
            detail=f"Source '{source_id}' not found. Upload a file or check DB connection.",
        )

    # Generate markdown
    try:
        markdown = await generate_knowledge_base(
            schema        = schema,
            domain        = domain,
            source_id     = source_id,
            model_manager = _model_mgr,
        )
    except RuntimeError as exc:
        raise HTTPException(status_code=503, detail=str(exc))

    # Save to knowledge store
    store = get_store()
    meta  = KBMetadata(
        source_id    = source_id,
        domain       = domain,
        generated_at = datetime.now().isoformat(timespec="seconds"),
        table_count  = len(schema.tables),
    )
    store.save(source_id, markdown, meta)

    # Hot-reload concepts and column notes into any existing pipeline
    concepts     = store.parse_concepts(source_id)
    column_notes = store.get_column_notes(source_id)
    if source_id in _pipelines:
        _pipelines[source_id].refresh_concepts(concepts)
        _pipelines[source_id].refresh_column_notes(column_notes)
    elif source_id in _schemas:
        _reload_upload_pipeline(source_id, _schemas[source_id])

    return {
        "source_id":     source_id,
        "domain":        domain,
        "concepts_found": len(concepts),
        "markdown_size": len(markdown),
        "generated_at":  meta.generated_at,
    }


# ── Knowledge Base CRUD ───────────────────────────────────────────────────────

@app.get("/api/kb/{source_id}", tags=["Knowledge Base"])
async def get_kb(source_id: str):
    """Return the current markdown knowledge base for a source."""
    store = get_store()
    md    = store.load_markdown(source_id)
    if md is None:
        raise HTTPException(status_code=404, detail=f"No KB found for '{source_id}'")
    meta = store.load_metadata(source_id)
    return {
        "source_id": source_id,
        "markdown":  md,
        "metadata":  meta.__dict__ if meta else None,
    }


@app.put("/api/kb/{source_id}", tags=["Knowledge Base"])
async def save_kb(source_id: str, req: SaveKBRequest):
    """
    Save user-edited markdown back to the knowledge store.
    Re-parses concepts so changes take effect immediately.
    """
    store = get_store()
    try:
        store.update_markdown(source_id, req.markdown)
    except FileNotFoundError as exc:
        raise HTTPException(status_code=404, detail=str(exc))

    # Hot-reload into pipeline
    concepts     = store.parse_concepts(source_id)
    column_notes = store.get_column_notes(source_id)
    if source_id in _pipelines:
        _pipelines[source_id].refresh_concepts(concepts)
        _pipelines[source_id].refresh_column_notes(column_notes)

    return {
        "saved":          True,
        "concepts_found": len(concepts),
        "source_id":      source_id,
    }


@app.delete("/api/kb/{source_id}", tags=["Knowledge Base"])
async def delete_kb(source_id: str):
    """Delete the KB for a source (forces re-generation on next use)."""
    store = get_store()
    if not store.exists(source_id):
        raise HTTPException(status_code=404, detail=f"No KB for '{source_id}'")
    store.delete(source_id)
    # Reset pipeline to no concepts
    if source_id in _pipelines:
        _pipelines[source_id].refresh_concepts([])
    return {"deleted": True, "source_id": source_id}


@app.get("/api/kb/{source_id}/status", tags=["Knowledge Base"])
async def kb_status(source_id: str):
    """Check if a KB exists for a source and return its metadata."""
    store = get_store()
    exists = store.exists(source_id)
    meta   = store.load_metadata(source_id) if exists else None
    return {
        "source_id": source_id,
        "exists":    exists,
        "metadata":  meta.__dict__ if meta else None,
    }


# ── Schema refresh ────────────────────────────────────────────────────────────

class SchemaRefreshRequest(BaseModel):
    db_source: DBSource


@app.post("/schema/refresh", tags=["System"])
async def refresh_schema(req: SchemaRefreshRequest):
    src_id  = _source_id_for(req.db_source)
    adapter = _adapters.get(src_id)
    if not adapter:
        raise HTTPException(status_code=404, detail=f"Source '{src_id}' not connected")
    invalidate_schema_cache(adapter)
    schema = await get_schema_cached(adapter, ttl=0)
    return {
        "refreshed": True,
        "source_id": src_id,
        "tables":    len(schema.tables),
        "database":  schema.database,
    }


# ── Query endpoint ────────────────────────────────────────────────────────────

@app.post("/api/ask", tags=["Query"])
async def api_ask(req: AskRequest):
    """
    Main chat endpoint. Converts a natural language question to SQL
    using the knowledge base for the given source.
    """
    source_id = req.source_id or _first_source_id()
    if not source_id:
        raise HTTPException(
            status_code=503,
            detail="No data source available. Upload a file or configure a database.",
        )

    pipeline = _get_pipeline_or_raise(source_id)
    scoped_session = f"{source_id}:{req.session_id}"

    result   = await pipeline.run(req.question, session_id=scoped_session)
    api_resp = result.to_api_response()

    columns = api_resp["data"]["columns"]
    rows    = api_resp["data"]["rows"]
    error   = api_resp.get("error")

    return {
        "answer":           _build_answer(req.question, columns, rows, error),
        "sql":              api_resp["sql"],
        "data":             api_resp["data"],
        "chart_type":       _infer_chart_type(columns, rows),
        "source_id":        source_id,
        "execution_time_ms": api_resp["timing"]["total_ms"],
        "row_count":        api_resp["data"]["row_count"],
        "follow_ups":       _generate_follow_ups(req.question, columns),
        "validation":       api_resp["validation"],
        "error":            error,
    }


