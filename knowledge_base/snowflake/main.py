# ── Helpers ───────────────────────────────────────────────────────────────────

def _first_source_id() -> Optional[str]:
    if _pipelines:
        return next(iter(_pipelines))
    if _schemas:
        return next(iter(_schemas))
    return None


def _get_pipeline_or_raise(source_id: str) -> NLPToSQLPipeline:
    p = _pipelines.get(source_id)
    if not p:
        raise HTTPException(
            status_code=404,
            detail=f"No active pipeline for source '{source_id}'. "
                   "Available: " + str(list(_pipelines.keys()))
        )
    return p


def _infer_chart_type(columns: list[str], rows: list[dict]) -> str:
    if not rows or not columns:
        return "none"
    if len(rows) == 1 and len(columns) == 1:
        return "number"
    if len(columns) >= 2:
        label_col = columns[0]
        value_col = columns[1]
        if any(kw in label_col.lower() for kw in ("date","month","year","day","time","week")):
            return "line"
        try:
            float(rows[0].get(value_col, ""))
            return "bar" if len(rows) <= 12 else "line"
        except (ValueError, TypeError):
            pass
    return "table"


def _generate_follow_ups(question: str, columns: list[str]) -> list[str]:
    q = question.lower()
    follow_ups = []
    if any(kw in q for kw in ("total","sum","revenue","sales")):
        follow_ups.append("Break this down by month")
    if "top" in q:
        follow_ups.append("Show the bottom performers instead")
    if any(kw in q for kw in ("revenue","sales","amount","profit")):
        follow_ups.append("What is the trend over the last 6 months?")
    if columns:
        follow_ups.append(f"Group by {columns[0]}")
    if not follow_ups:
        follow_ups = ["Show me more details", "Summarize this data"]
    return follow_ups[:3]


def _build_answer(question: str, columns: list, rows: list, error) -> str:
    if error:
        return f"I ran into an error: {error}"
    if not rows:
        return "The query returned no results. The data may not exist for that filter, or try rephrasing."
    if len(rows) == 1 and len(columns) == 1:
        val = rows[0].get(columns[0], "N/A")
        return f"The result is **{val}**."
    return f"Here are the results — **{len(rows)}** row(s) across **{len(columns)}** column(s)."


