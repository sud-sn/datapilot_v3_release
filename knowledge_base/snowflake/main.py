@app.get("/health", tags=["System"])
async def health():
    db_status = {}
    for src_id, adapter in _adapters.items():
        db_status[src_id] = await adapter.health_check()
    model_status = await _llm.health_check() if _llm else {}
    return {
        "status":     "ok" if (db_status or _schemas) else "upload-only",
        "databases":  db_status,
        "models":     model_status,
        "uploads":    list(_schemas.keys()),
    }


@app.get("/sources", tags=["System"])
async def list_sources():
    sources = []
    for src_id, adapter in _adapters.items():
        store  = get_store()
        has_kb = store.exists(src_id)
        sources.append({
            "id":       src_id,
            "name":     adapter.source_name,
            "dialect":  adapter.dialect.value,
            "type":     "database",
            "has_kb":   has_kb,
        })
    for src_id, schema in _schemas.items():
        store  = get_store()
        has_kb = store.exists(src_id)
        sources.append({
            "id":      src_id,
            "name":    schema.database,
            "dialect": schema.dialect.value,
            "type":    "upload",
            "has_kb":  has_kb,
        })
    return {"sources": sources}


