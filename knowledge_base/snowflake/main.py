# ── Application state ─────────────────────────────────────────────────────────
_adapters:  dict[str, PostgreSQLAdapter | AzureSQLAdapter] = {}
_pipelines: dict[str, NLPToSQLPipeline] = {}
_schemas:   dict[str, SchemaContext] = {}   # for uploaded files (no DB adapter)
_llm:       Optional[OllamaProvider] = None
_model_mgr: Optional[ModelManager]  = None


def _source_id_for(db_source: DBSource) -> str:
    return f"{db_source.value}"


def _make_pipeline(
    adapter,
    source_id: str,
) -> NLPToSQLPipeline:
    """Create a pipeline, pre-loading concepts and column notes from the KB store."""
    store        = get_store()
    concepts     = store.parse_concepts(source_id)   if store.exists(source_id) else None
    column_notes = store.get_column_notes(source_id) if store.exists(source_id) else {}
    return NLPToSQLPipeline(
        adapter          = adapter,
        llm              = _llm,
        schema_cache_ttl = get_settings().schema_cache_ttl,
        concept_registry = concepts,
        column_notes     = column_notes,
        source_id        = source_id,
    )


# ── Lifespan ──────────────────────────────────────────────────────────────────

@asynccontextmanager
async def lifespan(app: FastAPI):
    global _llm, _model_mgr
    s = get_settings()

    # Knowledge store
    store = init_store(s.knowledge_store_path)
    logger.info("Knowledge store: %s", s.knowledge_store_path)

    # Pre-load dialect knowledge bases
    get_all_knowledge_bases()

    # LLM providers
    _llm       = OllamaProvider()
    _model_mgr = ModelManager()
    await _llm.start()
    await _model_mgr.start()
    await _model_mgr.warmup_sql_model()

    # PostgreSQL
    if s.pg_host and s.pg_user and s.pg_database:
        try:
            pg        = PostgreSQLAdapter()
            await pg.connect()
            src_id    = _source_id_for(DBSource.POSTGRESQL)
            _adapters[src_id] = pg
            _pipelines[src_id] = _make_pipeline(pg, src_id)
            logger.info("PostgreSQL ready (source_id=%s)", src_id)
        except Exception as exc:
            logger.warning("PostgreSQL unavailable: %s", exc)

    # Azure SQL
    if s.az_server and s.az_user and s.az_database:
        try:
            az        = AzureSQLAdapter()
            await az.connect()
            src_id    = _source_id_for(DBSource.AZURE_SQL)
            _adapters[src_id] = az
            _pipelines[src_id] = _make_pipeline(az, src_id)
            logger.info("Azure SQL ready (source_id=%s)", src_id)
        except Exception as exc:
            logger.warning("Azure SQL unavailable: %s", exc)

    # Snowflake
    if s.sf_account and s.sf_user and s.sf_database and s.sf_warehouse:
        try:
            sf        = SnowflakeAdapter()
            await sf.connect()
            src_id    = _source_id_for(DBSource.SNOWFLAKE)
            _adapters[src_id] = sf
            _pipelines[src_id] = _make_pipeline(sf, src_id)
            logger.info("Snowflake ready (source_id=%s)", src_id)
        except Exception as exc:
            logger.warning("Snowflake unavailable: %s", exc)

    if not _pipelines:
        logger.warning("No DB connections — upload-only mode active")

    yield

    # Shutdown
    for adapter in _adapters.values():
        await adapter.disconnect()
    await _llm.stop()
    await _model_mgr.stop()


app = FastAPI(
    title       = "DataPilot",
    description = "NLP → SQL with AI-generated business knowledge base",
    version     = "2.0.0",
    lifespan    = lifespan,
)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["*"],
    allow_headers=["*"],
)

